package com.machine.exercise;

public interface Letter {

    char[][] buildLetter();

    char[][] getLetter();

    static void printVertically(char[][] grid) {
        for (int i = 0; i < grid.length; i++) {
            for (int j = 0; j < grid[i].length; j++) {
                System.out.print(grid[i][j] + " ");
            }
            System.out.println();
        }
    }
}
